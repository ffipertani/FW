package com.ff.fw.tags;

import com.ff.fw.template.Tag;

public class Button extends Tag{

	private String label;
	private int width;
	
	
	 
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public int getWidth() {
		return width;
	}
	public void setWidth(int width) {
		this.width = width;
	}

	
	
}
